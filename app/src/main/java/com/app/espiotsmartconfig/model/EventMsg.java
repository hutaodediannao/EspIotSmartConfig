package com.app.espiotsmartconfig.model;

public class EventMsg {
   public int code;
   public String data;

    public EventMsg(int code, String data) {
        this.code = code;
        this.data = data;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
